#include<iostream>
#include<conio.h>
using namespace std;
int main()
{
  int x=5,y=3;

  x+=y; //x=x+y
  cout<<"x value is: "<<x <<endl;

  x-=y; //x=x-y
  cout<<"x value is: "<<x <<endl;

  x*=y; //x=x*y
  cout<<"x value is: "<<x <<endl;

  x/=y; //x=x/y
  cout<<"x value is: "<<x <<endl;

  x%=y; //x=x%y
  cout<<"x value is: "<<x;

  getch();

} 